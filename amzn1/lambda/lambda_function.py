class MusicIntentHandler(AbstractRequestHandler):

    """Handler for Help Intent."""

    def can_handle(self, handler_input):
        # type: (HandlerInput) -
        return ask_utils.is_intent_name("MusicIntent")(handler_input)

    def handle(self, handler_input):
        global idstuff
        # type: (HandlerInput) -> Response

        genre = ask_utils.request_util.get_slot(
            handler_input, "MusicGenre").value

        artist = ask_utils.request_util.get_slot(
            handler_input, "MusicType").value

        idstuff = suno.starttask(artist, genre)

        speak_output = f"Making music with SUNO AI, Hang Tight!"

        return (
            handler_input.response_builder
            .speak(speak_output)
            .ask(speak_output)
            .response
        )


